package syntax07LoopWhile;

public class ForLOOPintro {

	public static void main(String[] args) {
		//Good morning 5 times
		for(int c=1; c<6;c++) {
			System.out.println("Good morning");
		}
		
		//print numbres from 1 to 10
		for (int i=0; i<=11; i++) {
			System.out.println(i);
		}
		System.out.println("-------------------");
		
		//print numbers from10 to1
		for (int i=10; i<=1; i--) {
			System.out.println(i);
			
		}
		
		System.out.println("===================");
		for (int i=0; i<=50; i+=1) {
			System.out.println(i);
		}
		System.out.println("&&&&&&&&&&&&&&&&&&&&");
		
		int sum=0;
		for(int i=0; i<5; i++) {
			System.out.println(sum+i);
}}}


