package syntax21THISs;
public class Addition {

void add(int num, int num1) {
System.out.println(num+num1);
}
void add1(int num1, int num2) {
System.out.println(num1+num2);
}
void add2(int num, int num1, int num2, int num3) {
System.out.println(num+num1+num2+num3);
}
//Method overloading by changing type of parameters
void add3(int num1,double num2) {
System.out.println(num1+num2);
}
void add(double num2, double num3) {
System.out.println(num2+num3);
}}

