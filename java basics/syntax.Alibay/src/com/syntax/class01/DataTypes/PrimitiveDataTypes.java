package com.syntax.class01.DataTypes;

public class PrimitiveDataTypes {

public static void main(String[] args) {
	
	//8 primitive datatypes
	
	//to store whole numbers-->byte,short,int,long
	
	byte variableOne=10;//-127 to 127
	
	short variableTwo=100;// -32767 to 32767
	
	int variableThree=1000;// - -2147483648 to 2147483647
	
	long variableFour=100000;// -9.........................
	
	//to store decimals numbers --> float,double
	
	float variableFive=12.99f;
	
	double variableSix=132.89;
	
	//to store single character -->char
	
	char variableSeven='F';///single quotation for char only!
	
	//to store boolean values --> boolean-->true or false only
	
	boolean hungry=true;
			
	boolean thirsty=false;
	
	
	System.out.println(variableOne);
	System.out.println(variableSeven);
	
	
	
	
}
}


